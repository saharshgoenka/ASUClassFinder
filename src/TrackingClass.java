public class TrackingClass {
    String classAbb;
    String classNumber;
    String teacherName;
    String wantedTime;
    int reservedForOthers;

    public TrackingClass(String classAbb, String classNumber, String teacherName, String wantedTime, int reservedForOthers) {
        this.classAbb = classAbb;
        this.classNumber = classNumber;
        this.teacherName = teacherName;
        this.wantedTime = wantedTime;
        this.reservedForOthers = reservedForOthers;
    }


}
